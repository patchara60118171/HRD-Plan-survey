#!/usr/bin/env node
/**
 * Rebuilds only tmhi.js and ucla.js from their .jsx source files.
 */
const fs   = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const ROOT       = path.resolve(__dirname, '..');
const DASHBOARDS = path.join(__dirname, 'dashboards');

// Ensure Babel
try { require.resolve('@babel/core', { paths: [ROOT] }); }
catch { execSync('npm install --no-save --silent @babel/core @babel/preset-react', { cwd: ROOT, stdio: 'inherit' }); }
const babel = require(require.resolve('@babel/core', { paths: [ROOT] }));

const HEADER = () =>
`/* auto-generated by rebuild-tmhi-ucla.js – do not edit */
(function () {
  'use strict';
  var React = window.React;
  var Recharts = window.Recharts;
  var useState = React.useState, useMemo = React.useMemo, useEffect = React.useEffect, useRef = React.useRef, Fragment = React.Fragment;
  var BarChart = Recharts.BarChart, Bar = Recharts.Bar, XAxis = Recharts.XAxis, YAxis = Recharts.YAxis,
      CartesianGrid = Recharts.CartesianGrid, Tooltip = Recharts.Tooltip, ResponsiveContainer = Recharts.ResponsiveContainer,
      RadarChart = Recharts.RadarChart, Radar = Recharts.Radar, PolarGrid = Recharts.PolarGrid,
      PolarAngleAxis = Recharts.PolarAngleAxis, PolarRadiusAxis = Recharts.PolarRadiusAxis,
      LineChart = Recharts.LineChart, Line = Recharts.Line, AreaChart = Recharts.AreaChart, Area = Recharts.Area,
      PieChart = Recharts.PieChart, Pie = Recharts.Pie, Cell = Recharts.Cell,
      ScatterChart = Recharts.ScatterChart, Scatter = Recharts.Scatter, ZAxis = Recharts.ZAxis,
      ComposedChart = Recharts.ComposedChart, Legend = Recharts.Legend, ReferenceLine = Recharts.ReferenceLine;

`;

const FOOTER = (key, component) =>
`\n\n  window.__DASHBOARDS__ = window.__DASHBOARDS__ || {};
  window.__DASHBOARDS__[${JSON.stringify(key)}] = ${component};
})();
`;

const FILES = [
  { key: 'exec',     component: 'ExecutiveSummary'  },
  { key: 'tmhi',     component: 'TMHIDashboard'     },
  { key: 'ucla',     component: 'UCLADashboard'     },
  { key: 'physical', component: 'PhysicalDashboard' },
  { key: 'env',      component: 'EnvDashboard'      },
];

for (const f of FILES) {
  let src = fs.readFileSync(path.join(DASHBOARDS, f.key + '.jsx'), 'utf8');

  // Strip the JSX_HEADER lines added by the original build.js
  src = src.replace(/^\/\* auto-generated[^\n]*\*\/\n?/m, '');
  // Strip destructuring imports that build.js adds at top of .jsx
  src = src.replace(/^const \{ useState[^\n]*\n?/m, '');
  src = src.replace(/^const \{[\s\S]*?\} = Recharts;\n?/m, '');
  // Strip JSX_FOOTER lines that build.js adds at bottom of .jsx
  src = src.replace(/\nwindow\.__DASHBOARDS__[\s\S]*$/, '');

  const input = HEADER() + src + FOOTER(f.key, f.component);

  const { code } = babel.transformSync(input, {
    presets: [['@babel/preset-react', { runtime: 'classic' }]],
    filename: f.key + '.js',
    babelrc: false,
    configFile: false,
  });

  fs.writeFileSync(path.join(DASHBOARDS, f.key + '.js'), code);
  console.log('✔', f.key + '.js', '(' + Math.round(code.length / 1024) + ' KB)');
}
console.log('Done.');
